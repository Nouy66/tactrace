    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        /**
         * *******************************************************
         */

        DefaultTableModel model = new DefaultTableModel();
        Test2 viewch = new Test2();
        ArrayList<VIChild> childlist = viewch.viewChildren();
        VIChild child;
        Object[] col = {"ID", "Name", "Age", "Gender", "Level", "Score"};
        model.setColumnIdentifiers(col);

        jTable1.setModel(model);
        jTable1.setBackground(Color.WHITE);
        jTable1.setForeground(Color.BLACK);
        jTable1.setRowHeight(20);

        Object[] row = new Object[6];

        for (int index = 0; index < childlist.size(); index++) {
            child = childlist.get(index);
            row[0] = child.getVIChildID();
            row[1] = child.getChildName();
            row[2] = child.getAge();
            row[3] = child.getGender();
            row[4] = child.getLevel();
            row[5] = child.getScore();
            model.addRow(row);
        }

    }//GEN-LAST:event_formWindowActivated

