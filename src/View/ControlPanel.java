    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        name_txt.setText(model.getValueAt(selectedRowIndex, 1).toString());
        age_txt.setText(model.getValueAt(selectedRowIndex, 2).toString());
        score_txt.setText(model.getValueAt(selectedRowIndex, 5).toString());//interdection between row&colomn
        String gender = model.getValueAt(selectedRowIndex, 3).toString();
        level_txt.setText(model.getValueAt(selectedRowIndex, 4).toString());

        if (gender.equals("M")) {
            male.setSelected(true);
            female.setSelected(false);
        } else {
            male.setSelected(false);
            female.setSelected(true);
        }

    }//GEN-LAST:event_jTable1MouseClicked

